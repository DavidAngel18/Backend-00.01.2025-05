let num1 = 4
switch (num1) {
    case 1:
        console.log(`Domingo`)
        break;
    case 2:
        console.log(`Lunes`)
        break;
    case 3:
        console.log(`Martes`)
        break;
    case 4:
        console.log(`Miercoles`)
        break;
    case 5:
        console.log(`Jueves`)
        break;
    case 6:
        console.log(`Viernes`)
        break;
    case 7:
        console.log(`Sabado`)
        break;

    default:
        console.log(`No es un numero para el dia de la semana`)
        break;
}